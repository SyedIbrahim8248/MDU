package JAVAandDSAKHAPRA;

public class _11_Printthenumbersfrom0to1 {
    public static void main(String[] args) {
        int i = 0;
        while (i < 11) {
            System.out.println(i);
            i = i + 1;
            int j = 0;
            do {
                System.out.println(j);
                j = j + 1;
            } while (j < 11);

        }
    }
}









